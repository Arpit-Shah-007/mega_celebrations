export function getPackageBySlug(slug: string): Package | undefined {
  return packages.find((item) => item.slug === slug)
}

export function getRelatedPackages(current: Package, count = 3): Package[] {
  const sharedTagMatches = packages
    .filter((item) => item.slug !== current.slug)
    .map((item) => ({
      item,
      score: item.tags.filter((tag) => current.tags.includes(tag)).length,
    }))
    .sort((a, b) => b.score - a.score)

  return sharedTagMatches.slice(0, count).map((entry) => entry.item)
}

